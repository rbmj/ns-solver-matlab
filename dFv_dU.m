function dFv_dU = dFv_dU(R, e, e_eta, e_xi, eta_x, eta_y, gamma, k, m, m_eta, m_xi, mu, n, n_eta, n_xi, rho, rho_eta, rho_xi, xi_x, xi_y)
  x0 = n_eta.*rho;
  x1 = n_xi.*rho;
  x2 = eta_x.*rho_eta;
  x3 = 4*m;
  x4 = m_eta.*rho;
  x5 = eta_y.*rho_eta;
  x6 = 2*n;
  x7 = rho_xi.*xi_x;
  x8 = m_xi.*rho;
  x9 = rho_xi.*xi_y;
  x10 = rho.^(-3);
  x11 = mu.*x10;
  x12 = x2 + x7;
  x13 = rho.^2;
  x14 = 1./x13;
  x15 = mu.*x14;
  x16 = x12.*x15;
  x17 = x5 + x9;
  x18 = x15.*x17;
  x19 = 2*m;
  x20 = 1./R;
  x21 = m.*rho_eta - x4;
  x22 = eta_y.*x21;
  x23 = m.*rho_xi - x8;
  x24 = x23.*xi_y;
  x25 = n.*rho_eta - x0;
  x26 = eta_x.*x25;
  x27 = n.*rho_xi - x1;
  x28 = x27.*xi_x;
  x29 = x26 + x28;
  x30 = 3*n;
  x31 = eta_y.*x25;
  x32 = x27.*xi_y;
  x33 = eta_x.*x21;
  x34 = x23.*xi_x;
  x35 = 2*x33 + 2*x34;
  x36 = R.*mu;
  x37 = eta_x.*n_eta + n_xi.*xi_x;
  x38 = 2*x26 + 2*x28;
  x39 = eta_x.*m_eta;
  x40 = m_xi.*xi_x;
  x41 = -2*x31 - 2*x32 + 4*x33 + 4*x34;
  x42 = e_eta.*eta_x + e_xi.*xi_x;
  x43 = x33 + x34;
  x44 = k.*(gamma - 1);
  x45 = m.*x12;
  x46 = x36/3;
  x47 = x10.*x20;
  x48 = n.*x12;
  dFv_dU = [0 0 0 0; 2*x11.*(-2*eta_x.*x4 + eta_y.*x0 + x1.*xi_y + x2.*x3 + x3.*x7 - x5.*x6 - x6.*x9 - 2*x8.*xi_x)/3 -4*x16/3 2*x18/3 0; x11.*(-eta_x.*x0 - eta_y.*x4 - x1.*xi_x + x19.*x5 + x19.*x9 + x2.*x6 + x6.*x7 - x8.*xi_y) -x18 -x16 0; x20.*(x36.*(x19.*(rho.*(-eta_y.*n_eta - n_xi.*xi_y + 2*x39 + 2*x40) + x41) + x30.*(rho.*(eta_y.*m_eta + m_xi.*xi_y + x37) + 2*x22 + 2*x24 + x38)) + x36.*(x19.*(-x31 - x32 + x35) + x30.*(x22 + x24 + x29)) + 3*x44.*(-m.*x43 - m.*(rho.*(x39 + x40) + x35) - n.*x29 - n.*(rho.*x37 + x38) + 2*rho.*(e.*x12 - rho.*x42) + x13.*x42))./(3*rho.^4) x47.*(x44.*(x43 + x45) + x46.*(-x17.*x30 - x41 - 4*x45)) x47.*(x44.*(x29 + x48) + x46.*(2*m.*x17 - 3*x22 - 3*x24 - 3*x26 - 3*x28 - 3*x48)) -x12.*x14.*x20.*x44];
end
