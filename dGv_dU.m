function dGv_dU = dGv_dU(R, e, e_eta, e_xi, eta_x, eta_y, gamma, k, m, m_eta, m_xi, mu, n, n_eta, n_xi, rho, rho_eta, rho_xi, xi_x, xi_y)
  x0 = eta_x.*rho_eta;
  x1 = 2*n;
  x2 = n_eta.*rho;
  x3 = eta_y.*rho_eta;
  x4 = 2*m;
  x5 = m_eta.*rho;
  x6 = rho_xi.*xi_y;
  x7 = m_xi.*rho;
  x8 = rho_xi.*xi_x;
  x9 = n_xi.*rho;
  x10 = rho.^(-3);
  x11 = mu.*x10;
  x12 = x3 + x6;
  x13 = rho.^2;
  x14 = 1./x13;
  x15 = mu.*x14;
  x16 = x12.*x15;
  x17 = x0 + x8;
  x18 = x15.*x17;
  x19 = 1./R;
  x20 = n.*rho_eta - x2;
  x21 = eta_x.*x20;
  x22 = n.*rho_xi - x9;
  x23 = x22.*xi_x;
  x24 = m.*rho_eta - x5;
  x25 = eta_y.*x24;
  x26 = m.*rho_xi - x7;
  x27 = x26.*xi_y;
  x28 = x25 + x27;
  x29 = 3*m;
  x30 = eta_x.*x24;
  x31 = x26.*xi_x;
  x32 = eta_y.*x20;
  x33 = 2*x32;
  x34 = x22.*xi_y;
  x35 = 2*x34;
  x36 = R.*mu;
  x37 = eta_y.*m_eta + m_xi.*xi_y;
  x38 = 2*x25 + 2*x27;
  x39 = 4*x32;
  x40 = 4*x34;
  x41 = eta_y.*n_eta;
  x42 = n_xi.*xi_y;
  x43 = e_eta.*eta_y + e_xi.*xi_y;
  x44 = x32 + x34;
  x45 = k.*(gamma - 1);
  x46 = m.*x12;
  x47 = x36/3;
  x48 = x10.*x19;
  x49 = n.*x12;
  dGv_dU = [0 0 0 0; x11.*(-eta_x.*x2 - eta_y.*x5 + x0.*x1 + x1.*x8 + x3.*x4 + x4.*x6 - x7.*xi_y - x9.*xi_x) -x16 -x18 0; 2*x11.*(eta_x.*m_eta.*rho + 4*eta_y.*n.*rho_eta - 2*eta_y.*x2 + m_xi.*rho.*xi_x + 4*n.*rho_xi.*xi_y - x0.*x4 - x4.*x8 - 2*x9.*xi_y)/3 2*x18/3 -4*x16/3 0; x19.*(x36.*(-x1.*(x30 + x31 - x33 - x35) + x29.*(x21 + x23 + x28)) + x36.*(-x1.*(rho.*(eta_x.*m_eta + m_xi.*xi_x - 2*x41 - 2*x42) + 2*x30 + 2*x31 - x39 - x40) + x29.*(rho.*(eta_x.*n_eta + n_xi.*xi_x + x37) + 2*x21 + 2*x23 + x38)) + 3*x45.*(-m.*x28 - m.*(rho.*x37 + x38) - n.*x44 - n.*(rho.*(x41 + x42) + x33 + x35) + 2*rho.*(e.*x12 - rho.*x43) + x13.*x43))./(3*rho.^4) x48.*(x45.*(x28 + x46) + x47.*(2*n.*x17 - 3*x21 - 3*x23 - 3*x25 - 3*x27 - 3*x46)) x48.*(x45.*(x44 + x49) + x47.*(2*eta_x.*x24 - x17.*x29 + 2*x26.*xi_x - x39 - x40 - 4*x49)) -x12.*x14.*x19.*x45];
end
